function settableData(data, index, length) {
    data.forEach((row, i) => {
        let obj = {
            id: i + 1, // ID 
            selectType: '',  // 选定
            sum_B: '', // 黑色
            sum_W: '', // 白色
            difference: '', // 差值
            total: '', // 统计
        }
        let column = []
        let tyleList = row.split('')

        for (let j = 0; j < length; j++) {
            obj[`${j + 1}`] = tyleList[j] || '-'
            obj[`del_${j + 1}`] = false
            if (i == 0) {
                column.push({ prop: `${j + 1}`, label: `${j + 1}` })
                if (obj[`${j + 1}`] != '-') {
                    obj[`num_${j + 1}`] = 1
                }

            } else {
                obj[`num_${j + 1}`] = ''
            }
        }
        switch (index) {
            case 1:
                tableData_1.push(obj)
                break;
            case 2:
                tableData_2.push(obj)
                break;
            case 3:
                tableData_3.push(obj)
                break;

        }
        if (i == 0) {
            switch (index) {
                case 1:
                    tableColumns_1 = column
                    break;
                case 2:
                    tableColumns_2 = column
                    break;
                case 3:
                    tableColumns_3 = column
                    break;
            }

        }

    })
}

